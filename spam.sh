MAGENTA='\x1b[35m'
CYAN='\x1b[36m'
RESET='\x1b[0m'

# Function to generate a random color
random_color() {
  local colors=("$RED" "$GREEN" "$YELLOW" "$BLUE" "$MAGENTA" "$CYAN")
  local index=$((RANDOM % ${#colors[@]}))
  echo "${colors[$index]}"
}

# Function to ask a question and read the answer
ask_question() {
  read -p "$1" answer
  echo "$answer"
}

# Function to request pairing codes
spam_pairing_code() {
  local phone_number=$1
  local num_codes=$2

  if ! [[ "$phone_number" =~ ^[0-9]+$ ]]; then
    echo -e "${RED}Error: Invalid phone number. Please enter digits only.${RESET}"
    return 1
  fi

  if ! [[ "$num_codes" =~ ^[0-9]+$ ]]; then
    echo -e "${RED}Error: Invalid number of codes. Please enter a positive integer.${RESET}"
    return 1
  fi

  if ((num_codes <= 0)); then
    echo -e "${RED}Error: Number of codes must be greater than zero.${RESET}"
    return 1
  fi

  echo -e "${YELLOW}Starting spam of $num_codes pairing codes to $phone_number...${RESET}"

  for ((i = 1; i <= num_codes; i++)); do
    # Replace this with the actual command to request the pairing code
    # For example, using curl:
    # code=$(curl -s "https://example.com/request_code?phone=$phone_number")
    # For demonstration purposes, let's just generate a random code:
    code=$(openssl rand -hex 4)
    code=$(echo "$code" | sed 's/\(....\)/\1-/g; s/-$//')

    if [[ -n "$code" ]]; then
      echo -e "${GREEN}Success Spam Pairing Code - Number : $phone_number from : [$i/$num_codes]${RESET}"
      echo "Code: $code"
    else
      echo -e "${RED}Error: Failed to retrieve pairing code for attempt $i.${RESET}"
    fi
    sleep 1 # Add a delay to avoid rate limiting
  done

  echo -e "${GREEN}Finished spamming pairing codes to $phone_number.${RESET}"
}

